    <h2>The company started in 1945 with a humble beginning has now grown to achieve annual turn of over $55 million and with a work force over 150 employees.

In the pursuit of excellence in service, we march forever forward as a team with a clear foresight open to new ideas and technology, developing on the existing systems, provide the best after-sale services for medical equipment in the island in terms of speed to response to calls minimum break down time, and maximum customer satisfactions.</h2>
		<p>The late Mr. Burhanuddin Al Awadhi (father of the present Chairman/CEO/Directors) founded our company in 1945. The Pharmacy was then located in the Manama city center. In the beginning, the Pharmacy was mainly involved in the trade of pharmaceuticals and general consumer items for the retail and wholesale market. Gradually, the arena of trade has been developed to include the public sector undertakings. Active participation in governmental and institutional sales was initiated by participating in tenders. Under the circumstances, the business had to go through wider areas of supplies to meet with the demands of the hospitals and clinics for various items and equipment.</p>
<p>As business developed, a new division, Medical & Scientific Division, was opened to look after the trade and service of medical and scientific equipment. This contributed to the tremendous growth of the Company. At this juncture, in the new scenario, as arena of business activities widened, a new company, Gulf Corporation for Technology, was incorporated. This was in 1995. GCT mainly looks after the sales and service of medical, scientific, educational and industrial equipment and supplies. The fields of action for GCT include hospitals, educational institutions, schools and universities, various industrial units and other developmental projects.

</p>
